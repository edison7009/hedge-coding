/// Supported documentation site formats
#[derive(Debug, Clone, Copy, PartialEq)]
pub enum DocFormat {
    /// Docusaurus v3 — MDX + sidebar_position frontmatter (most popular)
    Docusaurus,
    /// VitePress — Markdown + YAML frontmatter, Vue-based
    VitePress,
    /// GitBook — SUMMARY.md structure, clean Markdown
    GitBook,
    /// MkDocs Material — YAML nav, Markdown
    MkDocs,
    /// Plain Markdown — universal, framework-agnostic
    Markdown,
}

impl DocFormat {
    pub fn from_str(s: &str) -> Self {
        match s {
            "vitepress" => DocFormat::VitePress,
            "gitbook"   => DocFormat::GitBook,
            "mkdocs"    => DocFormat::MkDocs,
            "markdown"  => DocFormat::Markdown,
            _           => DocFormat::Docusaurus,
        }
    }

    pub fn label(&self) -> &'static str {
        match self {
            DocFormat::Docusaurus => "Docusaurus v3 (MDX)",
            DocFormat::VitePress  => "VitePress",
            DocFormat::GitBook    => "GitBook",
            DocFormat::MkDocs     => "MkDocs Material",
            DocFormat::Markdown   => "Plain Markdown",
        }
    }

    fn format_instructions(&self) -> &'static str {
        match self {
            DocFormat::Docusaurus => {
                r#"## Output Format: Docusaurus v3 (MDX)
Generate one MDX file per major module/component. Use this frontmatter for each file:
```mdx
---
sidebar_label: 'Component Name'
sidebar_position: 1
description: One-sentence description for SEO.
---
```
Use `:::tip`, `:::warning`, `:::info` admonitions where appropriate.
Code blocks must use triple backtick fences with language identifier.
Include a `docusaurus.config.js` sidebar structure recommendation at the end."#
            }
            DocFormat::VitePress => {
                r#"## Output Format: VitePress
Generate one Markdown file per major module. Use this frontmatter:
```md
---
title: Component Name
description: One-sentence description.
outline: deep
---
```
Use `## ` level-2 headers for main sections. Include a `.vitepress/config.ts` sidebar
configuration snippet at the end listing all generated pages."#
            }
            DocFormat::GitBook => {
                r#"## Output Format: GitBook
Generate a SUMMARY.md table of contents first, then one Markdown file per module.
SUMMARY.md format:
```md
# Table of Contents
* [Introduction](README.md)
* [API Reference](api/README.md)
  * [Module A](api/module-a.md)
```
Use standard Markdown. No JSX or special syntax."#
            }
            DocFormat::MkDocs => {
                r#"## Output Format: MkDocs Material
Generate one Markdown file per module. At the end, include the complete `mkdocs.yml` nav block:
```yaml
nav:
  - Home: index.md
  - API Reference:
    - Module A: api/module-a.md
```
Use standard Markdown headers. Use `!!! tip`, `!!! warning` admonitions (MkDocs Material style)."#
            }
            DocFormat::Markdown => {
                r#"## Output Format: Plain Markdown
Generate one `.md` file per major module/component.
Use standard CommonMark Markdown only — no JSX, no Vue, no special syntax.
At the end, provide an `index.md` that links to all generated pages.
This output works with any doc site generator."#
            }
        }
    }
}

/// Options for compiling a Docs Generation Super Prompt
pub struct DocCompileOptions<'a> {
    /// User's documentation goal
    pub goal: &'a str,
    /// Repo map for architecture understanding
    pub repo_map: &'a RepoMap,
    /// Selected source files (full content included)
    pub selected_files: &'a [FileEntry],
    /// Documentation site format
    pub format: DocFormat,
    /// Optional project memory
    pub project_memory: Option<&'a str>,
    /// Optional Deep Analysis summaries (from analysis_cache.json).
    /// Pre-computed by cheap models — reused as semantic shortcuts for the doc writer.
    pub file_summaries: Option<&'a str>,
}

/// Compile a Documentation Generation Super Prompt.
/// Unlike auto-doc tools (JSDoc, TypeDoc) that only parse syntax,
/// this gives the model the full source code + architecture context,
/// producing semantic, human-quality documentation.
pub fn compile_docs(options: &DocCompileOptions) -> Result<SuperPrompt> {
    let mut xml = String::new();
    let mut source_chars = 0;

    // --- Project Memory ---
    if let Some(mem) = options.project_memory {
        if !mem.trim().is_empty() {
            xml.push_str("<project_memory>\n");
            xml.push_str(mem);
            if !mem.ends_with('\n') { xml.push('\n'); }
            xml.push_str("</project_memory>\n\n");
        }
    }

    // --- Documentation Goal ---
    xml.push_str("<documentation_goal>\n");
    if options.goal.trim().is_empty() {
        xml.push_str("Generate comprehensive documentation for this project.");
    } else {
        xml.push_str(options.goal);
    }
    xml.push('\n');
    xml.push_str("</documentation_goal>\n\n");

    // --- Deep Analysis Summaries (from existing cache, no re-analysis needed) ---
    if let Some(summaries) = options.file_summaries {
        if !summaries.trim().is_empty() {
            xml.push_str("<file_intelligence>\n");
            xml.push_str("<!-- Pre-computed by cheap models via Deep Analysis. Use these summaries\n");
            xml.push_str("     as semantic shortcuts - they describe WHAT each file does and WHY. -->\n");
            xml.push_str(summaries);
            if !summaries.ends_with('\n') { xml.push('\n'); }
            xml.push_str("</file_intelligence>\n\n");
        }
    }

    // --- Repo Map (architecture understanding) ---
    xml.push_str("<repo_map>\n");
    xml.push_str("<!-- Full project structure — use this to understand architecture before writing docs -->\n");
    xml.push_str(&render_repo_map(options.repo_map));
    xml.push_str("</repo_map>\n\n");

    // --- Source Files (full content) ---
    xml.push_str("<source_files>\n");
    xml.push_str("<!-- Complete source code — read every file before writing any documentation -->\n");
    for file in options.selected_files {
        let content = match fs::read_to_string(&file.path) {
            Ok(c) => c,
            Err(e) => {
                xml.push_str(&format!("  <file path=\"{}\" error=\"{}\" />\n", file.relative_path, e));
                continue;
            }
        };
        source_chars += content.len();
        xml.push_str(&format!("  <file path=\"{}\">\n", file.relative_path));
        xml.push_str(&content);
        if !content.ends_with('\n') { xml.push('\n'); }
        xml.push_str("  </file>\n\n");
    }
    xml.push_str("</source_files>\n\n");

    // --- Documentation Instructions ---
    xml.push_str("<documentation_instructions>\n");

    // Role & philosophy
    xml.push_str(
        "You are a senior technical writer with deep software engineering expertise. \
        You have thoroughly read every file in source_files and understand the complete \
        architecture from the repo_map. Unlike automated doc tools that only parse \
        syntax, you understand WHY the code is structured this way.\n\n"
    );

    // Format-specific instructions
    xml.push_str(options.format.format_instructions());
    xml.push_str("\n\n");

    // Universal doc standards
    xml.push_str(
        "## Documentation Standards\n\
        For EVERY documented component, module, or function, include:\n\n\
        1. **Purpose** — What problem does this solve? Why does it exist?\n\
        2. **How it works** — A plain-English explanation of the implementation approach.\n\
        3. **API Reference** — All public functions, parameters, return types, and errors.\n\
           For each parameter: type, whether optional, default value, constraints.\n\
        4. **Usage Example** — A realistic, working code example. Not a toy example.\n\
        5. **Common Pitfalls** — What mistakes do developers typically make with this?\n\
        6. **Dependencies** — What does this module depend on, and what depends on it?\n\n\
        ## Required Doc Pages (generate all of these)\n\
        - **Getting Started** — Installation, first run, minimal working example\n\
        - **Architecture Overview** — How the major components interact (with ASCII diagram if helpful)\n\
        - **API Reference** — One page per module/component with full parameter docs\n\
        - **Configuration** — All configuration options with types, defaults, and examples\n\
        - **Troubleshooting** — Common errors and their fixes, based on actual error paths in the code\n\n\
        ## Quality Rules\n\
        - Write for a developer who has NEVER seen this codebase before.\n\
        - Explain the 'why', not just the 'what'. The code already shows the 'what'.\n\
        - Every code example must be complete and runnable — no `// ...` ellipsis for critical parts.\n\
        - If you notice undocumented edge cases or error conditions in the source, document them.\n\
        - Do NOT paraphrase variable names as documentation. '`name` is the name' is useless.\n\
        - Flag any public APIs that appear incomplete, dangerous, or inconsistently implemented.\n"
    );

    xml.push_str(&format!(
        "\n## Format: {}\n\
        Output each generated documentation file clearly separated with a header:\n\
        ```\n\
        === FILE: docs/getting-started.md ===\n\
        [file content here]\n\
        === END FILE ===\n\
        ```\n\
        After all files, output a brief **Documentation Summary** listing:\n\
        - Files generated\n\
        - Any APIs that lack sufficient context for full documentation\n\
        - Any architecture observations worth highlighting\n",
        options.format.label()
    ));

    xml.push_str("</documentation_instructions>\n");

    Ok(SuperPrompt {
        content: xml,
        file_count: options.selected_files.len(),
        source_chars,
    })
}
